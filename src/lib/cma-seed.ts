import type { AssetClassSlug } from "./investments/asset-class-slugs";
import type { AssetTypeId } from "./investments/asset-types";
import cmaDefaults from "./cma-defaults.generated.json";

export interface SeedAssetClass {
  name: string;
  /** Optional stable slug used to look up engine-referenced classes
   * (e.g. "inflation") without relying on a human-edited name. */
  slug?: AssetClassSlug;
  geometricReturn: number;
  arithmeticMean: number;
  volatility: number;
  pctOrdinaryIncome: number;
  pctLtCapitalGains: number;
  pctQualifiedDividends: number;
  pctTaxExempt: number;
  assetType: AssetTypeId;
}

export interface SeedModelPortfolio {
  name: string;
  description: string;
  allocations: { assetClassName: string; weight: number }[];
}

// Asset-class CMAs (geometric/arithmetic returns + volatility) and the
// correlation matrix (further below) are loaded from cma-defaults.generated.json,
// a committed artifact regenerated by scripts/recompute-cma.ts. Structural fields
// (name/slug/assetType/tax composition) are carried forward unchanged by that
// script; only geometricReturn/arithmeticMean/volatility and the correlations
// change on a recompute. Historically the geometric return + volatility came
// from a standard 14-asset CMA set, with arithmetic mean derived as
// `geometric + σ²/2` (log-normal convergence) — kept as a stored field because
// the engine reads it directly.
export const DEFAULT_ASSET_CLASSES: SeedAssetClass[] =
  cmaDefaults.assetClasses as SeedAssetClass[];

// Fail fast if the generated file drifts from engine expectations.
if (DEFAULT_ASSET_CLASSES.length !== 16) {
  throw new Error(
    `cma-seed: expected 16 asset classes, got ${DEFAULT_ASSET_CLASSES.length}`,
  );
}

