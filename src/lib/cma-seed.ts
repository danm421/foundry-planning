export interface SeedCorrelation {
  /** Asset-class name as it appears in DEFAULT_ASSET_CLASSES. */
  classA: string;
  classB: string;
  correlation: number;
}

// Pairwise correlations (91 upper-triangle pairs over the 14 non-inflation
// classes) are loaded from cma-defaults.generated.json (regenerated by
// scripts/recompute-cma.ts). Pairs omitted default to 0 (independent) when the
// matrix is reconstructed in memory — per the eMoney whitepaper (p.5), a missing
// pair is treated as independent. Inflation is intentionally absent from the
// matrix; it is modeled separately by the engine.
//
// Canonical storage is (classA, classB) with classA < classB alphabetically,
// but the matrix-builder tolerates either ordering, so the list is stored in
// matrix (upper-triangle) order for readability.
export const DEFAULT_CORRELATIONS: SeedCorrelation[] =
  cmaDefaults.correlations as SeedCorrelation[];

if (DEFAULT_CORRELATIONS.length !== 91) {
  throw new Error(
    `cma-seed: expected 91 correlations, got ${DEFAULT_CORRELATIONS.length}`,
  );
}
